    public void loadData(UsuarioServicio service, Usuario data) {
        try {

            for (Rol rol : service.getServicePositions().getRolName()) {
                comboRol.addItem(rol);
                if (data != null && data.getUsuarioRol().getRolId() == rol.getRolId()) {
                    comboRol.setSelectedItem(rol);
                }
            }

        } catch (Exception e) {

            e.printStackTrace();

        }

        if (data != null) {
            txtNombre.setText(data.getNombre());
            txtApellido.setText(data.getApellido());
//            if(data.getDate() != null) {
//                datePicker.setSelectedDate(data.getDate().toLocalDate());
//            }
            txtContrasena.setText(data.getContrasena());

        }
    }

    public Usuario getData() {
        String nombre = txtNombre.getText().trim();
        String apellido = txtApellido.getText().trim();
        String contrasena = txtContrasena.getText().trim();
        Rol positions = (Rol) comboRol.getSelectedItem();

        return new Usuario(0, nombre, apellido, contrasena, positions);

    }

