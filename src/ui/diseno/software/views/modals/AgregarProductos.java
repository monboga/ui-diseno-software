    public Producto getData() {
        String name = txtName.getText().trim();
        String description = txtDescription.getText().trim();
        String model = txtModel.getText().trim();
        double price = Double.parseDouble(txtPrice.getValue().toString());
        double cost = Double.parseDouble(txtCost.getValue().toString());
        int stock = 0;
        try {
            String stockText = txtStock.getText().trim();
            if(!stockText.isEmpty()) {
                stock = Integer.parseInt(stockText);
            } else {
                stock = 0;
            }
        } catch (NumberFormatException e) {
            System.err.println("Error: el valor de stock debe ser un numero entero");
        }

        Marca brands = (Marca) comboBrand.getSelectedItem();
        Categoria category = (Categoria) comboCategory.getSelectedItem();

        return new Producto(0, name,description,model ,price, cost, stock, brands, category);

    }

