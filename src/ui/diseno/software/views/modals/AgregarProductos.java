    public void loadData(ProductoServicio service, Producto data) {
        try {

            for (Marca pos : service.getServiceBrand().getAll()) {
                comboBrand.addItem(pos);
                if (data != null && data.getMarcas().getMarcaId() == pos.getMarcaId()) {
                    comboBrand.setSelectedItem(pos);
                }
            }
            for (Categoria pos : service.getServiceCategory().getAll()) {
                comboCategory.addItem(pos);
                if (data != null && data.getCategorias().getCategoriaId() == pos.getCategoriaId()) {
                    comboCategory.setSelectedItem(pos);
                }
            }

        } catch (Exception e) {

            e.printStackTrace();

        }

        if (data != null) {
            txtName.setText(data.getNombreProducto());
            txtDescription.setText(data.getDescripcionProducto());
            txtModel.setText(data.getModeloProducto());
            txtPrice.setValue(data.getPrecioProducto());
            txtCost.setValue(data.getCostoProducto());
            txtStock.setText(String.valueOf(data.getStock()));
        }
    }

